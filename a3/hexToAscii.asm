.global _start
_start:


// convert Hex to ASCII and store in array at 0x1000 

HexToASCII:
ldr r1, =Data 
ldr r2, =_Data
ldr r3, =ASCII
ldr r4, =0x1000 // destination array 

loop: 
cmp r1, r2
beq _HexToASCII
ldr r0, [r1], #4
cmp r0, #9
addgt r0, r0, #1 
ldrb r0, [r3, r0] 
strb r0, [r4], #1
b loop

_HexToASCII:
b _HexToASCII

	
.data 
.align 
Data:
.word 0xA, 0xB, 0xC, 0x1, 0x2, 0x4, 0x9, 0x3
.word 0xD, 0xF, 0xE, 0x7, 0x5, 0x6, 0x8, 0x0
_Data: 
ASCII:
.byte 0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38
.byte 0x39, 0x40, 0x41, 0x42, 0x43, 0x44, 0x45, 0x46
_ASCII:
.end